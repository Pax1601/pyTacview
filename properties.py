from datetime import datetime

class dateStr(float):
    def __new__(cls, content):
        try:
            value = datetime.strptime(content, "%Y-%m-%dT%H:%M:%S.%fZ")
        except:
            value = datetime.strptime(content, "%Y-%m-%dT%H:%M:%SZ")
        return value

